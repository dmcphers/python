def concat(arg):
    st_concat = ""
    for val in arg:
        st_concat += val
    return st_concat

def st_multiply(arg):
    st_multiply = arg * 5
    return st_multiply

def st_slice(arg):
    st_slice_st = arg[:7]
    return st_slice_st

def st_length(arg):
    st_len = len(arg)
    return st_len