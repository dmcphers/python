def summation(arg):
    total = 0
    for val in arg:
        total += val
    return total


def diff(arg):
    total = 0
    for val in arg:
        total -= val
    return total


def prod(arg):
    total = 1
    for val in arg:
        total *= val
    return total


def quot(arg):
    total = 100
    for val in arg:
        total /= val
    return total


